#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	if (n < 1)
		return (0);
	while (*s1++ == *s2++ && *(s1 - 1) && *(s2 - 1) && --n > 0)
		continue;
	return (*(s1 - 1) - *(s2 - 1));
}
